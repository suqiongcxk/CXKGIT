// HT中断锁函数
void Lock_HT_Interrupt(DMA_HandleTypeDef *hdma)
{
    g_ht_interrupt_locked = 1;
    
    // 直接写入CCR寄存器，确保HTIE位为0
    hdma->Instance->CCR &= ~DMA_CCR_HTIE;
    
    // 添加内存屏障，确保写入立即生效
    __DSB();
    __ISB();
    
    // 再次确认写入成功
    if(hdma->Instance->CCR & DMA_CCR_HTIE)
    {
        // 如果还有，强制清除
        hdma->Instance->CCR &= ~DMA_CCR_HTIE;
    }
}




